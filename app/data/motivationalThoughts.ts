export const getThoughtByDay = (dayOfYear: number): string => {
  // Ensure day is within valid range (1-365)
  const day = Math.max(1, Math.min(365, dayOfYear));
  return motivationalThoughts[day - 1];
};

export const getCurrentDayOfYear = (): number => {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const diff = now.getTime() - start.getTime();
  return Math.floor(diff / (1000 * 60 * 60 * 24));
};

export const getTodaysThought = (): string => {
  return getThoughtByDay(getCurrentDayOfYear());
};